              ON Tiempo.id = hechosAlquiler.tiempo_id
WHERE hechosAlquiler.estado_alquiler = 'Activo'
  AND hechosAlquiler.montoActual > hechosAlquiler.montoAnterior
GROUP BY Tiempo.mes, Tiempo.anio
